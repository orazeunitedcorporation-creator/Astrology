// ===== SCROLL REVEAL ANIMATION =====
function revealOnScroll() {
    const reveals = document.querySelectorAll('.service-card, .feature, .gallery-item, .contact-method');
    
    reveals.forEach((element) => {
        const windowHeight = window.innerHeight;
        const elementTop = element.getBoundingClientRect().top;
        const elementVisible = 100;
        
        if (elementTop < windowHeight - elementVisible) {
            element.classList.add('scroll-reveal', 'active');
        }
    });
}

// ===== PARALLAX EFFECT FOR COSMIC ANIMATION =====
function parallaxEffect() {
    const scrolled = window.pageYOffset;
    const cosmicAnimation = document.querySelector('.cosmic-animation');
    
    if (cosmicAnimation && scrolled < window.innerHeight) {
        cosmicAnimation.style.transform = `translateY(${scrolled * 0.5}px)`;
        cosmicAnimation.style.opacity = 1 - (scrolled / window.innerHeight);
    }
}

